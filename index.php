README for After Douglas Davis
===================================================================================

Statement
--------------

The World’s First Collaborative Sentence was created by Douglas Davis in 1994 and donated to the Whitney Museum of American Art in 1995. Much like today’s blog environments and methods for crowdsourcing knowledge, it allowed users to contribute to a never-ending sentence. 

At some point the sentence stopped functioning, and in early 2012 the <a href="http://whitney.org/Exhibitions/Artport/DouglasDavis" target="_blank">Whitney Museum undertook a "preservation effort"</a> to repair and relaunch the project. Measures were taken to during the "restoration" to stay true to the original intent of the artist, leaving dead links, and the original, though outdated, code.

During the preservation the curators placed small sections of garbled ASCII text from the original on Github with the hope that others would "fork" the data and repair the original. However, the Whitney Museum did not succeed in realizing that the collaborative culture of the net Davis that predicted has fully arrived. This is evident not only through sites like Wikipedia, Facebook, and Tumblr, but the open source movement, which brought us Linux, Apache, and PHP, the very technologies used to view this page, as well as others like Firefox, Arduino, Processing, <a href="http://en.wikipedia.org/wiki/Open-source_movement" target="_blank">and many more</a>. 

In the spirit of open source software and artists like <a href="https://www.google.com/search?q=duchamp+fountain" target="_blank">Duchamp</a>, <a href="https://www.google.com/search?q=sherrie%20Levine%20after%20walker%20evans" target="_blank">Levine</a>, and <a href="http://www.aftersherrielevine.com/" target="_blank">Mandiberg</a>, on September 5, 2013, I "forked" Douglas Davis' Collaborative Sentence by downloading all pages and constructing from scratch the functional code which drives the project. I have now placed this work on Github with the following changes:

1.  All pages are updated to HTML5 and UTF-8 character encoding
2.  The functional code was rewritten from scratch including a script to remove malicious code
3.  The addition of this statement


